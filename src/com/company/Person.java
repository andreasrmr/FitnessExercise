package com.company;

public class Person {
    String name;
    String cpr;

    public Person() {
    }

    public String getName() {
        return name;
    }

    public String getCpr() {
        return cpr;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setCpr(String cpr) {
        this.cpr = cpr;
    }
}
